"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

print(1234)

# Aspas simples
print('Julio Gazoli')
print('Juliio "Gazoli"')

# Aspas duplas
print("Julio Gazoli")
print("Julio 'Gazoli'")

# Escape
print("Julio \"Gazoli\"")

# r
print(r"Julio \"Gazoli\"")